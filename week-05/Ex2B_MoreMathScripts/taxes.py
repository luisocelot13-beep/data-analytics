# Federal taxes are 23% of your salary every month. You make X amount of money.
# How much is withheld for taxes?

monthly_income = 6000

tax_rate = 0.23

tax_withheld = monthly_income * tax_rate
print(f'with your monthly income being {monthly_income}, and the taxe rate being {tax_rate} %,  your tax withheld every month will be {tax_withheld:.0f}')